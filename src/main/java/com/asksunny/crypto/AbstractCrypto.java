	public final static int INTEGER_MASK = 0x0FFF;

	public static long checksum(byte[] bytes) {
		long chk = 0;
		for (int i = 0; i < bytes.length; i++) {
			chk = (chk + (int)bytes[i]);
		}
		return chk;
	}

	public static Key toSecurityKey(byte[] key) throws Exception {
		Key ret = null;
		MessageDigest md = MessageDigest.getInstance("SHA-256");
		byte[] mykeyval = md.digest(key);
		mykeyval = Arrays.copyOf(mykeyval, 16);
		ret = new SecretKeySpec(mykeyval, AES128);
		return ret;

	}

	public static byte[] aes_encrypt(byte[] plainText, byte[] keys, byte[] tweak)
			throws Exception {
		Cipher chiper = Cipher.getInstance(AES128);
		chiper.init(Cipher.ENCRYPT_MODE, toSecurityKey(keys));
		byte[] encVal = chiper.doFinal(plainText);
		return encVal;
	}

	public static byte[] aes_decrypt(byte[] encVal, byte[] keys, byte[] tweak)
			throws Exception {
		Cipher chiper = Cipher.getInstance(AES128);
		chiper.init(Cipher.DECRYPT_MODE, toSecurityKey(keys));
		byte[] decVal = chiper.doFinal(encVal);
		return decVal;
	}

	public static int log2(int bits) {
		int log = 0;
		if ((bits & 0xffff0000) != 0) {
			bits >>>= 16;
			log = 16;
		}
		if (bits >= 256) {
			bits >>>= 8;
			log += 8;
		}
		if (bits >= 16) {
			bits >>>= 4;
			log += 4;
		}
		if (bits >= 4) {
			bits >>>= 2;
			log += 2;
		}
		return log + (bits >>> 1);
	}

